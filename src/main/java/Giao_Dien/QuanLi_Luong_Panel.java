    /*
    Phương thức cho đọc dữ liệu CSDL hiển thị lên bảng
    */
    private  void TaiDuLieuVaoBang(){
        try {
         

                BangLuongDao dao = new BangLuongDao();
                List < BangLuong> list = dao.findTop(10);
                tblModel.setRowCount(0);
                for (BangLuong  it : list ){
                    tblModel.addRow(new Object[]{
                       it.getMaNhanVien(),it.getHoTenNV(), it.getThang(),it.getSoNgayNghi(),it.getSoGioLamThem(), it.getLuongCoBan(),it.getTinhLuong()});
                    tblModel.fireTableDataChanged();
                }
      
                 
                 
        } catch (Exception e) {
            e.printStackTrace();
            HienThiThongBao.ThongBaoLoi(parentform, e.getMessage(), "Lỗi");
        }
    }

