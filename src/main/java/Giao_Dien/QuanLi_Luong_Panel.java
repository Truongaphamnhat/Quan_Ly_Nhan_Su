    private void QLL_table_BangLuongMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_QLL_table_BangLuongMouseClicked
  
           
   //     Lấy dữ liệu từ bẩng quản lí lương
        try {
            int row = QLL_table_BangLuong.getSelectedRow();
            
            if(row >= 0){
                String id = (String) QLL_table_BangLuong.getValueAt(row, 0);
                BangLuongDao dao = new BangLuongDao();
                BangLuong BL = dao.findByMaNhanVien(id);
              NhanVienDao nvdao = new NhanVienDao();
              NhanVien NV = (NhanVien) nvdao.findById(id);
                
                if(BL != null){
                    QLL_txtMaNhanVien.setText(BL.getMaNhanVien());
                    QLL_txtHoTenNV.setText(BL.getHoTenNV());
                    QLL_txtSoNgayNghi.setText(String.valueOf(BL.getSoNgayNghi()));
                    QLL_txtGioLamThem.setText(String.valueOf(BL.getSoGioLamThem()));
                    QLL_txtChucVu.setText(NV.getChucVu());
                    QLL_txtSDT.setText(NV.getSDT());
                  
                   
                     if(QLL_txtChucVu.getText().equals("Trưởng Phòng")){
                                QLL_txtPhuCap.setText(String.valueOf((int)(BL.getTinhLuong()-
                                 ((2600000 *2/26)* (26 - Integer.parseInt(QLL_txtSoNgayNghi.getText())) +
                                 (Integer.parseInt(QLL_txtGioLamThem.getText()) * 50000)))));
                     }else if(QLL_txtChucVu.getText().equals("Nhân Viên")){
                         QLL_txtPhuCap.setText(String.valueOf((int)(BL.getTinhLuong() -
                                 ((2600000*1.5 /26)* (26 - Integer.parseInt(QLL_txtSoNgayNghi.getText())) +
                                 (Integer.parseInt(QLL_txtGioLamThem.getText()) * 25000)))));
              
                    }
                    
                   
                    //Cắt Chuỗi và lấy giá trị của chuỗi 
                    String[] splits = BL.getThang().split("/");
                    QLL_cbb_Thang.setSelectedItem(splits[0]);
                    QLL_cbb_Nam.setSelectedItem(splits[1]);

                    
                    QLL_lbl_TongLuong.setText(String.valueOf(BL.getTinhLuong()));
                }
            }
        } catch (Exception e) {
             e.printStackTrace();
            HienThiThongBao.ThongBaoLoi(parentform, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_QLL_table_BangLuongMouseClicked



